#เขียนฟังก์ชัน printdouble(s) ที่รับรายการ s ของจำนวนเต็ม จากนั้นให้พิจารณาข้อมูลในรายการทีละตัวตามลำดับ แล้วพิมพ์ค่าข้อมูลแต่ละตัวนั้นคูณด้วย 2 (ให้สั่งพิมพ์ข้อมูลในฟังก์ชันเลย)

#>>> printdouble([1,10,3,7,15])
#2
#20
#6
#14
#30

s = [1,10,3,7,15]
print(f'printdouble({s})')

for i in range(5):
    c = s[i] * 2
    print(c)
